# crear lista con cuatro elementos
mi_lista = ['Uno',2,'Tres',4]
print(mi_lista)

#agregar al final
mi_lista.append('Cinco')
print(mi_lista)

#agregar dos elementos al final
mi_lista.extend([6,'Siete'])
print('El contenido de la lista es: ', mi_lista)
mi_lista.reverse()
print('El contenido de la lista a la inversa es: ', mi_lista)